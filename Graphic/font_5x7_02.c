{
    uchar dat = 0, i = 0;
    for(i=0; i<7; i++)
    {
        dst[i] = 0;
        dst[i] |= ((src[0] & (0x01 << i)) ? ((uchar)0x80) : (uchar)0);
        dst[i] |= ((src[1] & (0x01 << i)) ? ((uchar)0x40) : (uchar)0);
        dst[i] |= ((src[2] & (0x01 << i)) ? ((uchar)0x20) : (uchar)0);
        dst[i] |= ((src[3] & (0x01 << i)) ? ((uchar)0x10) : (uchar)0);
        dst[i] |= ((src[4] & (0x01 << i)) ? ((uchar)0x08) : (uchar)0);
    }
}

