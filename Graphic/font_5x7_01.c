uchar convert_uchar(uchar src)
{
    uchar dst = 0, i = 0;
    for(i=0; i<8; i++)
    {
        dst |= (((uchar)src & (uchar)(0x01 << i)) ? ((uchar)(0x80 >> i)) : (uchar)0);
    }
}

